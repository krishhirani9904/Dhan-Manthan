export const getPropertyMarketValue = (propertyId, completedImprovements) => {
  const prop = PROPERTIES.find(p => p.id === propertyId);
  if (!prop) return 0;
  const improvements = PROPERTY_IMPROVEMENTS[propertyId] || [];
  const done = completedImprovements || [];
  const improvementValue = improvements
    .filter(imp => done.includes(imp.id))
    .reduce((sum, imp) => sum + imp.valueIncrease, 0);
  return prop.price + improvementValue;
};

export const getPropertyRentalIncome = (propertyId, completedImprovements) => {
  const prop = PROPERTIES.find(p => p.id === propertyId);
  if (!prop) return 0;
  const improvements = PROPERTY_IMPROVEMENTS[propertyId] || [];
  const done = completedImprovements || [];
  const rentalBonus = improvements
    .filter(imp => done.includes(imp.id))
    .reduce((sum, imp) => sum + imp.rentalIncrease, 0);
  return prop.rentalPerHour + rentalBonus;
};